/** الحصول على نص المهنة حسب الـ id والجنس */
export function getSpecialtyLabel(id: number, gender: 'male' | 'female'): string {
  for (const cat of OCCUPATIONS) {
    const sp = cat.specialties.find(s => s.id === id);
    if (sp) return gender === 'female' ? sp.f : sp.m;
  }
  return 'غير محدد';
}

/** الحصول على المجال الكامل بالـ id */
export function getCategoryById(id: number): OccupationalCategory | undefined {
  return OCCUPATIONS.find(c => c.id === id);
}

/** قائمة مسطحة بكل الاختصاصات */
export function getAllSpecialties(): (Specialty & { categoryLabel: string })[] {
  return OCCUPATIONS.flatMap(cat =>
    cat.specialties.map(sp => ({ ...sp, categoryLabel: cat.label }))
  );
}